'use strict'
let number1 = 5;
let number2 = 4;
/**
 * Сложение
 * @param {number} number1 
 * @param {number} number2 
 * @returns {number}
 */
function plus(number1, number2) {
    return number1 + number2;
}
/**
 * Вычитание
 * @param {*} number1 
 * @param {*} number2 
 * @returns {number}
 */
function minus(number1, number2) {
    return number1 - number2;
}
/**
 * Умножение
 * @param {*} number1 
 * @param {*} number2 
 * @returns {number}
 */
function multiply(number1, number2) {
    return number1 * number2;
}
/**
 * Деление
 * @param {*} number1 
 * @param {*} number2 
 * @returns {number}
 */
function divide(number1, number2) {
    return number1 / number2;
}